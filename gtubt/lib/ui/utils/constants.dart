    <p>b) The election of the members of the Board shall be made within ten days after the nomination and announcement of the candidates. The unified ballot lists for candidates nominated by political party groups shall be issued in separate list. Votes shall be cast by marking the specific place across the names of the candidates. Votes given more than the number of members to be elected to the Board from the quota of political party groups determined in accordance with the second paragraph shall be deemed invalid.</p>

    <p>c) Provided that the quorum of decision is ensured, candidates who have the most of the votes for the number of vacancies shall be deemed to have been elected.</p>

    <p>ç) The election for the renewal of the members shall be held two months before the expiration of their term of office; should there be a vacancy in the membership positions for any reason, there shall be an election within one month as of the date of vacancy; or if the date of vacancy coincides with the recess of the Grand National Assembly of Turkey, the election shall take place within one month from the end of the recess, by employing the same procedure. During these elections, the allocation of the vacant membership positions to the political party groups shall be made by considering the number of the elected members from the political party groups’ quotas in the first election and the current proportions of the political party groups.</p>

    <p>(6) Forty-five days before the expiration of the term of office or in case of expiration of term of office by any reason of the members elected by the President of the Republic of Turkey (…) <sup>(1)</sup>, the Authority shall notify the Presidency of the Republic of Turkey of the situation in fifteen days (…). A new election shall take place one month before the expiration of term of office of the members. Should there be a vacancy in these memberships before the expiration of term of office, there shall be an election within fifteen days as of the date of notification.<sup>(1)</sup></p>

    <p>(7) The Board shall elect the Head and the Second Head of the Board among its members. The Head of the Board is also the President of the Authority.</p>

    <p>(8) Term of office of the Board members is four years. Members may be re-elected after expiration of their term of office. The person who is elected for the position of the member whose post ends before the expiration of his or her term of office for any reason, shall serve the remaining period.</p>

    <p>(9) Members of the Board shall take the following oath before the First Presidency Board of the Court of Cassation: "I do solemnly swear on my honour and on my dignity that I will carry out my duties with absolute impartiality, correctness, fairness and with sense of justice in line with the Constitution and the relevant legislation." Application to Court of Cassation for oath taking is deemed to be one of the pressing matters.</p>

    <p>(10) Unless provided for by a specific law, the members shall not assume any public or private tasks other than those related with carrying out their official duties in the Board; shall not act as executives in associations, foundations, cooperatives and in similar bodies; shall not engage in commercial activities, shall not engage in self-employment, shall not act as arbitrators and expert witnesses. However, Board members may prepare scientific publications, give lectures and attend conferences provided that these will not hinder their primary duties, and may receive copyrights and fees associated with those.</p>

    <p>(11) Investigations into the claims about the crimes allegedly committed by the members in connection with their duties shall be conducted as per the Law No. 4483 of 2/12/1999 on Adjudication of Public Servants and Other Public Employees, and permission for investigation shall be granted by the President of Turkey.<sup>(1)</sup></p>

    <p>(12) Provisions of the Law No. 657 shall be applied to disciplinary investigations and prosecutions about the members of the Board.</p>

    <p>(13) Members shall not be removed from their office by any reason before the expiration of their term of office. However, members of the Board may be removed from office by the Board decision if:</p>

    <p>a) it is found out subsequently that they do not meet the conditions required for their election,</p>

    <p>b) the conviction for the crimes, which is rendered for crimes committed by them in connection with their duties, is finalised.</p>

    <p>c) it is ascertained with a medical board report that they will not be able to fulfil their duties,</p>

    <p>ç) it is ascertained that they have been absent from work for fifteen consecutive days or for a total of thirty days within a year, without legitimate permission and excuse.</p>

    <p>d) it is ascertained that they fail to attend three Board meetings in one month and ten Board meetings in one year without any permission and excuse.</p>

    <p>(14) Those who are appointed as the members of the Board shall be removed from their previous posts during their term of office in the Board. On the condition that they do not fail to meet the requirements of being employed as a civil servant, those who are assigned as Board members whilst on duty shall be appointed to posts that are appropriate for their vested positions and titles in one month, in case their term of office ends or they express their will to resign and lodge an application in this regard to their former institution within thirty days. Until the assignment, Authority shall continue to make any payment they are vested with. Until they take another post or take up another employment, Authority shall continue to make the payment of those who are appointed as Board members despite not being public servants and whose term of office terminated as stated hereinabove; and the payments to be made under this scope shall not exceed three months. With regard to personal and other rights, terms spent in the Authority shall be deemed to have spent in the previous institutions or organisations.</p>

    <p><b>Duties and powers of the Board</b></p>

    <p><b>ARTICLE 22</b> - (1) Duties and powers of the Board are as follows:</p>

    <p>a) to ensure that the personal data are processed in compliance with fundamental rights and freedoms.</p>

    <p>b) to conclude the complaints of those who claim that their rights with regard to personal data protection have been violated.</p>

    <p>c) to examine whether the personal data are processed in compliance with the laws, upon complaint, or ex officio where it learns about the alleged violation, and to take temporary measures, if necessary.</p>

    <p>ç) to determine the adequate measures which are necessary for the processing of special categories of personal data.</p>

    <p>d) to ensure that Data<b> </b>Controllers’ Registry is maintained.</p>

    <p>e) to carry out regulatory acts on the matters concerning the Board’s field of duty and operation of the Authority.</p>

    <p>f) to carry out regulatory act to determine obligations related to data security </p>

    <p>g) to carry out regulatory acts on the matters concerning duties, powers and responsibilities of the data controller and of its representative.</p>

    <p>ğ) to decide on the imposition of administrative sanctions provided for in this Law.</p>

    <p>h) to deliver its opinion about draft legislation prepared by other institutions or organizations that contain provisions on personal data.</p>

    <p>ı) to conclude the Strategic Plan of the Authority; to determine the purpose, objectives, service quality standards and performance criteria of the Authority.</p>

    <p>i) to discuss and decide on Strategic Plan and the budget proposal of the Authority which are prepared in compliance with its purposes and objectives.</p>

    <p>j) to approve and publish the draft reports on the performance, financial situation, annual activities and other matters related with the Authority.</p>

    <p>k) to discuss and decide on the recommendations as regards the purchase, sale and lease of immovable properties.</p>

    <p>l) to carry out other tasks provided for by laws.</p>

    <p><b>Working Principles of the Board</b></p>

    <p><b>ARTICLE 23</b> - (1) President shall determine the dates and agenda of the meetings. The President may summon the Board for an extraordinary meeting, if necessary.</p>

    <p>(2) The Board shall convene at least with six members, including the President, and shall take decisions by simple majority of its total members. Members of the Board shall not cast abstaining vote.</p>

    <p>(3) Members shall not attend and cast vote in meetings, which concern issues regarding themselves, their relatives by blood up to third degree and relatives by affinity of marriage up to second degree, their adopted children and their spouses even if the marriage has ended.</p>

    <p>(4) Members of the Board shall not disclose the secrets they have learned concerning the relevant persons and third parties during their work to anyone other than legally authorized bodies, neither shall they use such secrets for their benefits. This obligation shall continue even after the end of their term of office.</p>

    <p>(5) The issues debated in the Board shall be recorded in the minutes. The decisions and the grounds for the counter vote, if any, shall be written within 15 days at the latest. The Board shall release the decisions to the public, it deems necessary.</p>

    <p>(6) Unless otherwise agreed, debates at the Board meetings are confidential. </p>

    <p>(7) The Working procedures and principles of the Board and the writing procedure of the decisions and other issues shall be regulated through a by-law.</p>

    <p><b>The President</b></p>

    <p><b>ARTICLE 24</b> - (1) The President, who is the highest-level official of the Authority, as the Head of both the Authority and the Board, organises and conducts the services of the Authority in accordance with the legislation, Authority’s purpose and policies, Strategic Plan, performance criteria and service quality standards, and , ensures coordination between service units.</p>

    <p>(2) The President is responsible for the general management and representation of the Authority. This responsibility entails the duties and powers concerning regulation, execution, inspection, evaluation of Authority’s work and, its announcement to the public, when necessary.</p>

    <p>(3) The duties of the President are as follows;</p>

    <p>a) to chair the Board's meetings.</p>

    <p>b) to ensure the notification of Board decisions and public announcement of these when deemed necessary by the Board, and to monitor their implementation.</p>

    <p>c) to appoint Vice President, Heads of Departments and Authority’s personnel.</p>

    <p>ç) to finalize the recommendations communicated by service units and submit them to the Board.</p>

    <p>d) to ensure the implementation of the Strategic Plan and to establish the human resources and working policies in line with service quality standards.</p>

    <p>e) to prepare the annual budget and financial tables of the Authority in line with the determined strategies, annual purposes and objectives.</p>

    <p>f) to ensure coordination between the Board and service units to have them work incoherent, efficient, disciplined and well-ordered manner.</p>

    <p>g) to maintain the relations of the Authority with other institutions.</p>

    <p>ğ) to determine the scope of the duties and powers of the personnel authorized to sign on behalf of the President.</p>

    <p>h) to carry out other duties related to the management and operation of the Authority</p>

    <p>(4) The Second President is entitled to act on behalf of the President in his/her absence.</p>

    <p><b>Composition and Duties of the Presidency</b></p>

    <p><b>ARTICLE 25 -</b> (1) The Presidency is composed of Vice President and service units. The Presidency shall fulfil the duties listed in paragraph four through the service units which are organized as departments. The number of departments shall not be more than seven.</p>

    <p>(2) One Vice President shall be appointed by the President in order to assist him/her in his/her administrative duties.</p>

    <p>(3) The Vice President and Heads of Departments shall be appointed by the President among those who have a bachelor degree of at least 4 year program and served in the public institutions for at least ten years.</p>

    <p>(4) The duties of the Presidency are as follows,</p>

    <p>a) to maintain the Data Controllers’ Registry.</p>

    <p>b) to carry out clerical services for the Authority and the Board.</p>

    <p>c) to represent the Authority through attorneys-at-law at the proceedings and enforcement proceedings to which the Authority is a party; to follow up such proceedings or have them followed up and carry out the legal services.</p>

    <p>ç) to carry out personnel-related services of the Board members and Authority’s personnel.</p>

    <p>d) to perform the duties referred to in laws with regard to financial services and strategy development units.</p>

    <p>e) to ensure that the information systems are established and used in order to carry operations of the Authority.</p>

    <p>f) to draft reports on the annual activities of the Authority or on other issues which are deemed needed, and submit them to the Board.</p>

    <p>g) to draft the Strategic Plan of the Authority.</p>

    <p>ğ) to determine the personnel policy of the Authority, prepare and implement the education and career-based plans for the personnel.</p>

    <p>h) to carry out the appointment, transfer, discipline, performance, promotion, retirement and other similar procedures regarding the personnel.</p>

    <p>ı) to determine the ethical principles for the personnel and provide necessary training.</p>

    <p>i)to carry out the services with regard to purchasing, leasing, maintenance, repair, construction, archive, health and social issues and similar ones within the framework of the Public Financial Management and Control Law No. 5018 of 10/12/2003.</p>

    <p>j) to keep record of the movable and immovable properties of the Authority</p>

    <p>k) to fulfil other duties conferred by the Board or the President.</p>

    <p>(5) Service units and their working procedures and principles shall be laid down through a by-law which is brought into force by President of the Republic of Turkey in compliance with the field of activity, duties and powers stated in the Law upon Authority’s proposal.</p>

    <p><b>The Personal Data Protection Experts and the Assistant Experts</b></p>

    <p><b>ARTICLE 26 - </b>(1) The Personal Data Protection Experts and the Assistant Experts may be recruited by the Authority. The experts and assistant experts who are appointed as Personal Data Protection Expert within the framework of additional Article 41 of the Law No. 657 shall receive one extra grade for once only.</p>

    <p><b>Provisions on the Personnel and Personnel Rights</b></p>

    <p><b>ARTICLE 27- </b>(1) Personnel of the Authority shall be subject to the Law No. 657, excluding the matters regulated through this Law.</p>

    <p>(2) Head and members of the Board and personnel of the Authority shall receive remunerations determined to be paid to the precedent personnel, within the scope of financial and social rights, as per Additional Article 11 of the Decree Law No. 375 of 27/6/1989, within the framework of the same procedures and principles applicable. Among the remunerations paid to the precedent personnel, those which are exempt from taxes and other legal deductions shall also be exempt from taxes and deductions as per the Law.</p>

    <p>(3) Head and members of the Board and personnel of the Authority are subject to the sub-paragraph (c) of the first paragraph of Article 4 of the Social Insurance and General Health Insurance Law No. 5510 of 31/5/2006. Head and members of the Board and personnel of the Authority shall be considered equal with the precedent personnel in terms of retirement</p>

    <p>rights. Among the personnel who were appointed as Head and members of the Board when insured under sub-paragraph (c) of the first paragraph of Article 4 of the Law No. 5510, terms of office in these duties shall be considered while ascertaining acquired rights, pensions, grades and steps of those whose term of office ends or who express their will to resign. The relevant term of office of those who fall within the scope of Provisional Article 4 of the Law No. 5510 while on duty, shall be deemed as the period for which position and representation compensation should be paid. Removal from previous institutions and organisations of those who were appointed as Head and members of the Board when insured under sub-paragraph (a) of the first paragraph of Article 4 of the Law No. 5510, shall not entail receiving a severance pay or termination pay. In such a case, term of office qualified for a severance pay or redundancy payment, shall be added to the service periods spent as Head and member of the Board, and accepted as the period for which a retirement bonus.</p>

    <p>(4) Civil servants working in public administrations attached to the centralized government, social security institutions, local administrations, administrations attached to local administrations, local administrative unions, revolving fund enterprises, funds established with laws, public entities, organizations more than 50% of whose capital belongs to public, public economic enterprises, state-owned economic enterprises, and associations and establishments attached to these, as well as other public officials may be seconded to the Authority upon the consent of their own institution, judges and public prosecutors may be seconded upon their consent provided that their salaries, allowances, any increases thereof, compensations and other social and financial rights and aids are paid by their own institution. Requests of the Authority in this regard shall be concluded with priority by the related institutions and organizations. Personnel assigned accordingly shall be deemed on paid leave. During this leave, rights of the personnel and their connection with civil service shall be maintained, this period of leave shall be taken into account in promotions and retirement, and they shall be promoted in due time without any need to further action. Periods spent in the Authority by those assigned under this Article shall be deemed to have been spent in their own institutions. Number of the personnel assigned accordingly shall not exceed ten per cent of the total number of posts for Personal Data Protection Experts and Personal Data Protection Assistant Experts, and the term of assignment shall not exceed two years. However, when deemed necessary, this term may be extended in one-year periods.</p>

    <p>(5) Titles and numbers of posts regarding the personnel to be employed in the Authority are presented in the annexed Table (I). Changes in titles and grade; addition of new titles and annulment of vacant posts shall be made upon the decision of the Board, provided that it shall not exceed the total number of posts, and shall be limited with the titles in the annexed tables of the Decree Law No. 190 on the General Posts and Procedures, dated 13/12/1983.</p>

