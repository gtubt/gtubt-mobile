    <p><b>FIRST CHAPTER </b></p>

    <p><b>Purpose, Scope and Definitions</b></p>

    <p><b>Purpose</b></p>

    <p><b>ARTICLE 1 – </b>(1) The purpose of this Law is to protect fundamental rights and freedoms of persons, particularly the right to privacy, with respect to processing of personal data and to set forth obligations, principles and procedures which shall be binding upon natural or legal persons who process personal data.</p>

    <p><b>Scope</b></p>

    <p><b>ARTICLE 2 – </b>(2) The provisions of this Law shall apply to natural persons whose personal data are processed and to natural or legal persons processing such data wholly or partially by automated means or by non-automated means which provided that form part of a data filing system. </p>

