    <p><b>CHAPTER SIX</b></p>

    <p><b>The Personal Data Protection Authority and its Organization</b></p>

    <p><b>The Personal Data Protection Authority</b></p>

    <p><b>ARTICLE 19</b> - (1) Personal Data Protection Authority, which is a public legal entity and has administrative and financial autonomy, has been established to carry out duties conferred on it under this Law.</p>

    <p>(2) The Authority is affiliated to the Minister assigned by the President of the Republic.</p>

    <p>(3) The Headquarters of the Authority is in Ankara</p>

    <p>(4) The Authority is composed of the Board and the Presidency. Decision making body of the Authority is the Board.</p>

    <p><b>Duties of the Authority</b></p>

    <p><b>ARTICLE 20 -</b> (1) The duties of the Authority are as follows;</p>

    <p>(a) to follow the latest developments in the legislation and practices, make evaluations and recommendations, conduct researches and investigations or have them conducted, within its field of duty.</p>

    <p>(b) to cooperate with public institutions and organisations, non-governmental organizations, professional associations or universities within its field of duty, if needed.</p>

    <p>(c) to follow and evaluate the latest international developments on personal data; and within its field of duty cooperate with international organisations and participate in the meetings</p>

    <p>(ç) to transmit the annual activity report to the Presidency of the Republic of Turkey, the Committee on Human Rights Inquiry of Grand National Assembly of Turkey.</p>

    <p>(d) to carry out other duties provided by laws.</p>

    <p><b>Personal Data Protection Board</b></p>

    <p><b>ARTICLE 21</b> - (1) The Board shall perform and exercise the duties and powers conferred on it under this law and other legislation, independently and under its own responsibility. No body, authority, office or person shall give orders and instructions, recommendations or suggestions to the Board on matters falling within the scope of its duties and powers.</p>

    <p>(2) The Board consists of nine members. Five members of the Board shall be elected by the Grand National Assembly of Turkey; four members shall be elected by the President of the Republic of Turkey.</p>

    <p>(3) The following conditions shall be met to be a member of the Board:</p>

    <p>a) to have knowledge and experience on the issues in the field of duty of the Authority.</p>

    <p>b) to have the necessary qualifications specified in points (1), (4), (5), (6) and (7) of sub-paragraph (A) of first paragraph of Article 48 of the Civil Servants Law No. 657 of 14/7/1965.</p>

    <p>c) Not being a member of any political party.</p>

    <p>ç) Having a bachelor degree of at least four years.</p>

    <p>d) <b>(Repealed: 2/7/2018 – Decree Law – Article 703/163)</b></p>

    <p>(4) <b>(Obsolete: 2/7/2018 – Decree Law – Article 703/163)</b></p>

