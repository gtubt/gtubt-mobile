    <p><b>Miscellaneous</b></p>

    <p><b>Exemptions</b></p>

    <p><b>ARTICLE 28</b> (1) The provisions of this Law shall not be applied in the following cases where:</p>

    <p>a) personal data are processed by natural persons within the scope of purely personal activities of the data subject or of family members living together with him/her in the same dwelling provided that it is not to be disclosed to third parties and the obligations about data security is to be complied with.</p>

    <p>b) personal data are processed for official statistics and provided that they are being anonymized for the purposes for such as research, planning and statistics.</p>

    <p>(c) personal data are processed with artistic, historical, literary or scientific purposes, or within the scope of freedom of expression provided that national defence, national security, public security, public order, economic security, right to privacy or personal rights are not violated or the process doesn’t constitute a crime.</p>

    <p>(ç) personal data are processed within the scope of preventive, protective and intelligence activities carried out by public institutions and organizations duly authorised and assigned by law to maintain national defence, national security, public security, public order or economic security.</p>

    <p>(d) personal data are processed by judicial authorities or execution authorities with regard to investigation, prosecution, judicial or execution proceedings.</p>

    <p>(2) Provided that it is in compliance with and proportionate to the purpose and fundamental principles of this Law, Article 10 regarding the data controller's obligation to inform, Article 11 regarding the rights of the data subject, excluding the right to claim compensation, and Article 16 regarding the obligation to register with the Data Controllers’ Registry shall not be applied in the following cases where personal data processing:</p>

    <p>a) is necessary for the prevention of committing a crime or for crime investigation.</p>

    <p>b) is carried out on the data which are made public by the data subject himself/herself.</p>

    <p>c) is necessary for performance of supervision or regulatory duties and disciplinary investigation and prosecution to be carried out by the assigned and authorised public institutions and organizations and by public professional organizations, in accordance with the power conferred on them by the law,</p>

    <p>ç) is necessary for protection economic and financial interests of State related to budget, tax and financial matters.</p>

