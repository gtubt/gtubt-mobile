    <p><b>ARTICLE 29 -</b> (1) The budget of the Authority shall be prepared and adopted in accordance with procedures and principles provided for in the Law No. 5018.</p>

    <p>(2) The revenues of the Authority are as follows;</p>

    <p>a) Treasury grants from the general budget.</p>

    <p>b) The revenues from the movable and immovable properties of the Authority.</p>

    <p>c) Donations and grants received.</p>

    <p>ç) The revenues from the utilization of the revenues.</p>

    <p>d) Other revenues.</p>

