    <p><b>Rights and Obligations</b></p>

    <p><b>Obligation of Data Controller to Inform</b></p>

    <p><b>ARTICLE 10 – </b>(1) At the time when personal data are obtained, the data<b> </b>controller or the person authorised by it is obliged to inform the data subjects about the following:</p>

    <p>a) the identity of the<b> </b>data controller and of its representative, if any,</p>

    <p>b) the purpose of processing of personal data;</p>

    <p>c) to whom and for which purposes the processed personal data may be transferred,</p>

    <p>ç) the method and legal basis of collection of personal data,</p>

